-- A função populate_allergens_from_foods lê cada item da sua tabela foods e insere duas novas entradas correspondentes
-- na tabela allergens (uma para ALERGIA e outra para INTOLERANCIA), evitando duplicatas caso o nome já exista.
CREATE OR REPLACE FUNCTION populate_allergens_from_foods()
RETURNS void AS $$
BEGIN
    INSERT INTO allergens (name, type, is_approved)

    -- Primeiro, seleciona todos os alimentos para o tipo 'ALERGIA'
    SELECT
        f.name,
        'ALERGIA' AS type,
        TRUE AS is_approved
    FROM
        foods f
    UNION ALL -- Empilha os resultados com a próxima seleção

    -- Segundo, seleciona os mesmos alimentos para o tipo 'INTOLERANCIA'
    SELECT
        f.name,
        'INTOLERANCIA' AS type,
        TRUE AS is_approved
    FROM
        foods f
    -- Cláusula para evitar erros de duplicidade
    ON CONFLICT (LOWER(name)) DO NOTHING;
END;
$$ LANGUAGE plpgsql;
